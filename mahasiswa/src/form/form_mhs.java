    public void TampilData_Mhs() {
        TblModel_Mahasiswa = new DefaultTableModel();
        TblModel_Mahasiswa.addColumn("NIS");
        TblModel_Mahasiswa.addColumn("NAMA MAHASISWA");
        TblModel_Mahasiswa.addColumn("JENIS KELAMIN");
        TblModel_Mahasiswa.addColumn("TANGGAL LAHIR");
        TblModel_Mahasiswa.addColumn("ALAMAT");
        TblModel_Mahasiswa.addColumn("IPK");
        TblModel_Mahasiswa.addColumn("JURUSAN");
        TblModel_Mahasiswa.addColumn("SEMESTER");

        // utk set lebar nama kolom
        table_mahasiswa.setModel(TblModel_Mahasiswa);
        table_mahasiswa.getColumnModel().getColumn(0).setPreferredWidth(50);    // NIS
        table_mahasiswa.getColumnModel().getColumn(1).setPreferredWidth(140);   // NAMA MAHASISWA
        table_mahasiswa.getColumnModel().getColumn(2).setPreferredWidth(100);   // JENIS KELAMIN
        table_mahasiswa.getColumnModel().getColumn(3).setPreferredWidth(120);   // TANGGAL LAHIR
        table_mahasiswa.getColumnModel().getColumn(4).setPreferredWidth(60);    // ALAMAT
        table_mahasiswa.getColumnModel().getColumn(5).setPreferredWidth(40);    // ALAMAT
        table_mahasiswa.getColumnModel().getColumn(6).setPreferredWidth(120);   // JURUSAN
        table_mahasiswa.getColumnModel().getColumn(7).setPreferredWidth(100);   // SEMESTER

        try {
            Connection conn = koneksi.getConnection();
            java.sql.Statement stmt = conn.createStatement();
            SQL = " SELECT * FROM tb_mahasiswa "
                    + " INNER JOIN tb_jurusan ON tb_mahasiswa.id_jurusan = tb_jurusan.id_jurusan "
                    + " INNER JOIN tb_semester ON tb_mahasiswa.id_semester = tb_semester.id_semester ";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                TblModel_Mahasiswa.addRow(new Object[]{
                    res.getString("nis"),
                    res.getString("nama"),
                    res.getString("jenis_kelamin"),
                    res.getString("tgl_lahir"),
                    res.getString("alamat"),
                    res.getString("ipk"),
                    res.getString("nama_jurusan"),
                    res.getString("nama_semester"),});
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }

    }

