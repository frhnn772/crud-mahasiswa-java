    private void table_mahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_mahasiswaMouseClicked
        // TODO add your handling code here:
        try {
            int baris = table_mahasiswa.getSelectedRow();
            txtField_nis.setText(TblModel_Mahasiswa.getValueAt(baris, 0).toString());
            txtField_nama.setText(TblModel_Mahasiswa.getValueAt(baris, 1).toString());
            String jenis = TblModel_Mahasiswa.getValueAt(baris, 2).toString();
            if (jenis.equals("Laki - Laki")) {
                Rbtn_lk.setSelected(true);
            } else if (jenis.equals("Perempuan")) {
                Rbtn_pr.setSelected(true);
            }
            txtField_tgl_lahir.setText(TblModel_Mahasiswa.getValueAt(baris, 3).toString());
            txtArea_alamat.setText(TblModel_Mahasiswa.getValueAt(baris, 4).toString());
            txtField_ipk.setText(TblModel_Mahasiswa.getValueAt(baris, 5).toString());
            cbb_jurusan.setSelectedItem(TblModel_Mahasiswa.getValueAt(baris, 6).toString());
            cbb_semester.setSelectedItem(TblModel_Mahasiswa.getValueAt(baris, 7).toString());
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_table_mahasiswaMouseClicked

