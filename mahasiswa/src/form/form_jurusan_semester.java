    private void hapus_jurusanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapus_jurusanActionPerformed
        // TODO add your handling code here:
        try{
        Connection conn = koneksi.getConnection();
        java.sql.PreparedStatement stmt = conn.prepareStatement("DELETE FROM tb_jurusan WHERE nama_jurusan = '" + txtField_jurusan.getText() + "'");
        stmt.executeUpdate();
        PreparedStatement stmr = conn.prepareStatement("ALTER TABLE tb_jurusan AUTO_INCREMENT=0");
        stmr.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus", "pesan", JOptionPane.INFORMATION_MESSAGE);
        TampilData_Jurusan();
        txtField_jurusan.setText("");
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Dihapus" + e.getMessage(), "pesan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_hapus_jurusanActionPerformed

