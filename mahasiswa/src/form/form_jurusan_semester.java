    private void tambah_jurusanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambah_jurusanActionPerformed
        // TODO add your handling code here:
        if (txtField_jurusan.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Tidak Ada Jurusan Yang Ditambahkan!", "Pesan", JOptionPane.INFORMATION_MESSAGE);
        }else{
        try {
                Connection conn = koneksi.getConnection();
                PreparedStatement stmt = conn.prepareStatement(" INSERT INTO tb_jurusan(nama_jurusan) VALUES (?)");
                stmt.setString(1, txtField_jurusan.getText());
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Berhasil Menambahkan Jurusan Baru", "Pesan", JOptionPane.INFORMATION_MESSAGE);
                TampilData_Jurusan();
                txtField_jurusan.setText("");
            } catch (SQLException e) {
                System.out.println(e.getMessage());
        }
        }
    }//GEN-LAST:event_tambah_jurusanActionPerformed

