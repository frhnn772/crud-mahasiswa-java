    public void TampilData_Jurusan() {
        TblModel_Jurusan = new DefaultTableModel();
        TblModel_Jurusan.addColumn("ID");
        TblModel_Jurusan.addColumn("JURUSAN");
        
        table_jurusan.setModel(TblModel_Jurusan);
        
        try {
            Connection conn = koneksi.getConnection();
            java.sql.Statement stmt = conn.createStatement();
            SQL = "SELECT * FROM tb_jurusan";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
            TblModel_Jurusan.addRow(new Object[]{
                res.getString("id_jurusan"),
                res.getString("nama_jurusan"),
                });
                
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
